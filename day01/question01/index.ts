/*  Question 01:
        Install Node.js, TypeScript and VS Code on your computer/Laptop.

        Explain & TIP : Simply install the following things via any browser of your choice.     
*/


/*

Node.js Installation:

        Open any web browser and navigate to the official Node.js website: Node.js Downloads
        Download the recommended version for your operating system (Windows, macOS, or Linux) by clicking on the appropriate installer.
        Once the download is complete, run the installer and follow the on-screen instructions to install Node.js.
        After installation, you can verify if Node.js is installed correctly by opening a terminal (Command Prompt on Windows or Terminal on macOS/Linux) and typing:
        
        node -v
        This command should display the installed Node.js version.

TypeScript Installation (Optional):

        TypeScript can be installed globally via npm, which comes with Node.js. Open a terminal and run the following command:
        
        npm install -g typescript
        After installation, you can verify if TypeScript is installed correctly by typing:
        
        tsc -v
        This command should display the installed TypeScript version.


Visual Studio Code Installation:

        Open any web browser and navigate to the official Visual Studio Code website: VS Code Downloads
        Download the installer for your operating system.
        Once the download is complete, run the installer and follow the on-screen instructions to install Visual Studio Code.
        After installation, you can launch VS Code from your applications menu or by typing code in the terminal/command prompt.

Configuration (Optional):

        After installing Visual Studio Code, you might want to install some extensions for better TypeScript support or additional features. You can do this by navigating to the Extensions view in VS Code (click on the square icon on the sidebar) and searching for extensions like "TypeScript" or any other extensions you find useful for your development workflow.
        
That's it! You now have Node.js, TypeScript, and Visual Studio Code installed on your computer/laptop. You're ready to start developing TypeScript applications using VS Code.






*/

